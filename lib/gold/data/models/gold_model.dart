import '../../domin/entites/gold.dart';

class GoldModel extends Gold {
  const GoldModel({
    required super.timestamp,
    required super.metal,
    required super.currency,
    required super.exchange,
    required super.symbol,
    required super.prevClosePrice,
    required super.openPrice,
    required super.lowPrice,
    required super.highPrice,
    required super.openTime,
    required super.price,
    required super.ch,
    required super.chp,
    required super.ask,
    required super.bid,
    required super.priceGram24k,
    required super.priceGram22k,
    required super.priceGram21k,
    required super.priceGram20k,
    required super.priceGram18k,
    required super.priceGram16k,
    required super.priceGram14k,
    required super.priceGram10k,
  });

  factory GoldModel.fromJson(Map<String, dynamic> json) => GoldModel(
        timestamp: json['timestamp'],
        metal: json['metal'],
        currency: json['currency'],
        exchange: json['exchange'],
        symbol: json['symbol'],
        prevClosePrice: json['prev_close_price'],
        openPrice: json['open_price'],
        lowPrice: json['low_price'],
        highPrice: json['high_price'],
        openTime: json['open_time'],
        price: json['price'],
        ch: json['ch'],
        chp: json['chp'],
        ask: json['ask'],
        bid: json['bid'],
        priceGram24k: json['price_gram_24k'],
        priceGram22k: json['price_gram_22k'],
        priceGram21k: json['price_gram_21k'],
        priceGram20k: json['price_gram_20k'],
        priceGram18k: json['price_gram_18k'],
        priceGram16k: json['price_gram_16k'],
        priceGram14k: json['price_gram_14k'],
        priceGram10k: json['price_gram_10k'],
      );
}
